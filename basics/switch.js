const month = "march"

switch (month) {
    case "jan":
        console.log("January");
        break; //agar break nhi likhuga toh ?,,aur yeh case match hota hein toh ?,,,yeh statement + aur uske baad ka sara code execute hoga except default case
    case "feb":
        console.log("feb");
        break;
    case "march":
        console.log("march");
        break;
    case "april":
        console.log("april");
        break;

    default:
        console.log("default case match");
        break;
}//op-->march
